    <h5>Qual o primeiro passo para castrar o meu animal?</h5>
    <div class="respostas">
        <p>
            Para que a castração aconteça de maneira correta e segura, você deve procurar um profissional de confiança, nesse caso um médico 
            veterinário habilitado e com boas referências, que esclarecerá todas as suas dúvidas a respeito da preparação, do procedimento e dos 
            cuidados pós-operatórios. Ele fará alguns exames prévios para verificar a saúde do seu animalzinho e te instruirá quanto ao preparo para 
            a cirurgia que consiste num jejum de 8 a 12 horas (a depender da orientação do veterinário). O jejum é importantíssimo, pois caso o animal 
            tenha algum alimento no estômago no momento da aplicação da anestesia poderá vomitar desacordado e engasgar com o próprio vômito, o que 
            pode ser FATAL. Então muita atenção com o JEJUM. O procedimento é indolor devido à aplicação de anestesia geral, sendo a inalatória a mais 
            indicada. Após a cirurgia, o bichinho deve passar por uma observação clínica de algumas horas até ser liberado para alta.
        </p>
    </div>
    
    <h5>Quais cuidados devo ter no pós-operatório?</h5>

    <div class="respostas">
        <p>
            O cuidado do gatinho recém-castrado deve iniciar desde o transporte para casa. Opte por leva-lo de carro, dentro da caixinha de transporte, 
            em baixa velocidade. Minimize todos os impactos possíveis. Ao chegar em casa, minimize também os obstáculos o mantendo em um ambiente sem móveis
            para que ele permaneça sempre no chão e não faça esforços desnecessários que podem causar dores, inflamações e infecções na circuncisão. Faça 
            uma caminha quentinha e aconchegante e deixe-o à vontade.
            É muito comum que ele adote um comportamento mais reservado e letárgico no início: no primeiro dia, geralmente por conta do efeito da anestesia e em 
            seguida por conta da mudança na rotina. Respeite esse momento evitando brincadeiras e movimentos. Mantenha a água e a ração como de costume. Siga a agenda 
            de remédios rigorosamente. Nesse link você pode aprender a como dar comprimidos pro seu gatinho, ainda que ele seja bravo: https://www.youtube.com/watch?v=tVkE4t4QOpc.
            Os remédios são receitados pelo médico veterinário antes ou depois do procedimento e, no geral, consistem em algum medicamento para evitar dor (o que mais usamos é o 
            Dipirona gotas, é um remédio de gosto muito ruim então a melhor maneira de ministra-lo é dissolver as gotinhas em um pouquinho de água, e dar na seringa sem agulha); 
            alguma pomada ou spray para a cicatrização (o que mais usamos é a Rifocina Spray, medicamento humano que precisa de receita); e um Antibiótico para evitar infecções 
            (geralmente utilizamos Baytril ou Chemitril 50mg -1/2 comprimido por dia durante 7 dias a depender da cicatrização-).
            Atenção!: Esta não é a uma indicação, não medique o seu gatinho sem acompanhamento do médico veterinário responsável.
            Outro cuidado importante é quanto à manutenção dos curativos no caso de castração de fêmeas. O curativo deve ser trocado diariamente ou em dias intercalados a depender 
            da orientação médica (geralmente trocamos em dias intercalados). Você pode levar o seu bichinho até a clínica ou realizar a troca do curativo em casa. Neste vídeo você 
            encontra dicas e algumas repostas para as principais dúvidas que envolvem castração de fêmea https://www.youtube.com/watch?v=gvVz-I-sArc. Aqui tem um tutorial de como 
            fazer uma roupinha cirúrgica similar às vendidas nos pet shops https://youtu.be/XtUxQxeUKQk. As roupinhas protegem o curativo da sua gatinha de agentes externos que podem 
            causar infecções e evitam também que ela retire o curativo. No caso de machos, não é necessário curativo, pois se trata de uma incisão muito pequena, só é importante não 
            deixar que ele lamba o local e nem tente arrancar os pontos, o que pode ser evitado com o colar elisabetano.
            Os materiais que geralmente utilizamos para curativos de fêmeas são: Gaze; Esparadrapo (prende melhor mas pode irritar e machucar a pele do gatinho), fita crepe ou baby fix; 
            Fita microporosa (largura 5cm); Ataduras que podem substituir a roupinha mas devem ser trocadas sempre que houver troca do curativo (largura 10cm).
            A cada troca, analise atentamente a cicatrização. Caso observe sinais de inflamação como pruridos ou qualquer outro aspecto estranho na cicatrização, isso vale para os machos, 
            pois ainda que não tenha curativo, deve ser aplicado diariamente o remédio cicatrizante, tire uma foto e envie para o médico veterinário ou para quem intermediou a castração, 
            se for o caso.
