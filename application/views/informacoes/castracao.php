    <p>A castração consiste na retirada dos testículos/óvulos e útero do animal, impedindo a sua procriação e a consequente superpopulação de 
    gatos, o maior fator causador do grande número de gatinhos abandonados nas ruas. Mas as vantagens dessa prática vão muito além do 
    controle populacional. A castração elimina ou reduz quase em 100% as chances do seu gatinho desenvolver tumores de mama, de ovário de 
    útero ou de próstata, aumentando assim a sua longevidade. E é claro que você deseja que o seu gatinho viva com saúde e por mais tempo ao seu lado, não é mesmo?!
    A esterilização também extingue praticamente todos os transtornos que acompanham o período do cio, como fugas, marcação de território e o comportamento sexual, 
    fazendo com que a calma e a tranquilidade de machos e fêmeas possam ser mantidas sempre.</p>

    <h5>O meu gatinho é muito brincalhão, ele vai mudar a sua personalidade após a castração?</h5>
    <div class="respostas">
        <p>
