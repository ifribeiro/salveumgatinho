            seu aparelho sexual e reprodutivo. Com a baixa de testosterona nos machos e de progesterona nas fêmeas, o desejo de sair às ruas em busca 
            de acasalamento diminui. Por esse motivo ele permanece mais em casa, come mais e dorme mais, o que justifica também o aumento de peso. 
            Portanto, quando desperto e disposto, o seu gatinho continuará enérgico e peralta, pois trata-se de uma mudança em determinados aspectos 
            comporto sexual, e não da personalidade.
